// 便捷取中文名
const nm = (s) => DIMENSION_NAMES[s.key] || s.label || s.key

// ============ 报告主入口 ============
function generateReport(scores, phone) {
  const sorted = [...scores].sort((a, b) => b.score - a.score)
  const top3 = sorted.slice(0, 3)
  const bottom3 = [...scores].sort((a, b) => a.score - b.score).slice(0, 3)
  const avgScore = Math.round(scores.reduce((s, d) => s + d.score, 0) / scores.length)
  const totalScore = scores.reduce((s, d) => s + d.score, 0)

  const sections = [
    // —— 优势层 ——
    generateCover(phone),
    generatePortrait(scores, top3, bottom3, avgScore),
    generateCoreStrengths(top3),
    generateBehaviorLoop(top3, bottom3),
    // —— 数据层 ——
    generateOverview(scores, avgScore, totalScore, top3, bottom3),
    generateDimensionDetail(scores),
    // —— 职业与发展层 ——
    generateCareerDirection(scores, top3, bottom3),
    generateActionPlan(top3, bottom3),
  ]

  return sections.join('\n\n---\n\n').replace(/\\n/g, '\n')
}

// ============ 1. 封面 ============
function generateCover(phone) {
  const masked = phone ? `${phone.slice(0, 3)}****${phone.slice(-4)}` : '用户'
  const date = new Date().toLocaleDateString('zh-CN', { year: 'numeric', month: 'long', day: 'numeric' })

  return `## 📘 你的7维职业能力优势报告

<div align="center">

**求职小队 · 职业规划研究中心**

报告生成日期：${date}
测评用户：${masked}

---

*这不是一张把你定格的标签。*

*它先帮你认领自己的优势，再把最常出现的行为模式拆成可观察、可调整的回路。*

*把优势用足，比把短板补齐更值钱。*

</div>`
}

// ============ 2. 先认领这张画像 ============
function generatePortrait(scores, top3, bottom3, avgScore) {
  const topKey = top3[0].key
  const topLabel = nm(top3[0])
  const secondKey = top3[1]?.key || topKey
  const secondLabel = nm(top3[1] || top3[0])

  const variance = scores.reduce((sum, s) => sum + Math.pow(s.score - avgScore, 2), 0) / scores.length
  const stdDev = Math.sqrt(variance)
  const consistencyNote = stdDev > 12
    ? '这次答案前后一致，七种力量也拉开了足够差异，可以先用这张画像对照近期行为。'
    : '你的七维能力整体比较均衡，画像的轮廓不会特别鲜明——这本身就是一个值得关注的特点。可以先通读全文，找到最让你觉得"对，这就是我"的那几个段落。'

  const portrait = buildPortraitText(topKey, secondKey, topLabel, secondLabel)

  return `## 🔍 先认领这张画像

${portrait}

#### 这次结果可以先信到什么程度

${consistencyNote}它描述的是当前更常出现的入口，不是永远不变的身份。`
}

