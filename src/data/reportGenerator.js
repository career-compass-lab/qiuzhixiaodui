function generateCrossAnalysis(top3, bottom3) {
  const topKeys = top3.map(s => s.key)
  const bottomKeys = bottom3.map(s => s.key)
  const sorted = [...top3, ...bottom3].sort((a, b) => b.score - a.score)
  const top2Keys = sorted.slice(0, 2).map(s => s.key)
  const bottom2Keys = sorted.slice(-2).map(s => s.key)

  const synergy = generateSynergyAnalysis(topKeys)
  const conflict = generateConflictAnalysis(topKeys, bottomKeys)
  const blindspot = generateBlindspotAnalysis(topKeys, bottomKeys)
  const layering = generateLayering(top2Keys, bottom2Keys)

  return `## ⚙️ 能力怎样配合：协同·张力·盲区

${layering}

${synergy}
${conflict}
${blindspot}

> 💡 最前面的入口不等于最好的入口，最后面的也不等于最差的。每种力量在不同场景下都有它的最佳位置。你越能看清它们之间的配合关系，就越能在关键时刻切换出最合适的组合。`
}

