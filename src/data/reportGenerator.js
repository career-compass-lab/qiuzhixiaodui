function getIndustryHeatmap(scores) {
  const industries = [
    { name: '互联网/科技', required: { learning: 85, communication: 70, resilience: 75, execution: 85, teamwork: 80, innovation: 95, leadership: 70 } },
    { name: '金融/咨询', required: { learning: 85, communication: 85, resilience: 85, execution: 90, teamwork: 75, innovation: 65, leadership: 80 } },
    { name: '教育/培训', required: { learning: 80, communication: 90, resilience: 65, execution: 65, teamwork: 80, innovation: 70, leadership: 75 } },
    { name: '制造业/供应链', required: { learning: 60, communication: 60, resilience: 80, execution: 95, teamwork: 85, innovation: 55, leadership: 65 } },
    { name: '医疗/健康', required: { learning: 85, communication: 80, resilience: 90, execution: 75, teamwork: 85, innovation: 70, leadership: 65 } },
    { name: '文化创意', required: { learning: 70, communication: 80, resilience: 65, execution: 60, teamwork: 75, innovation: 95, leadership: 55 } },
  ]

  const userMap = Object.fromEntries(scores.map(s => [s.key, s.score]))

  const ranked = industries.map(ind => {
    const dims = ['learning', 'communication', 'resilience', 'execution', 'teamwork', 'innovation', 'leadership']
    const weighted = dims.reduce((sum, k) => {
      const u = userMap[k] || 0
      const r = ind.required[k] || 0
      if (u >= r) return sum + r
      return sum + u * 0.5
    }, 0)
    const max = dims.reduce((sum, k) => sum + (ind.required[k] || 0), 0)
    const fit = Math.round((weighted / max) * 100)
    const gaps = dims
      .map(k => ({ label: DIMENSION_NAMES[k], gap: (userMap[k] || 0) - ind.required[k] }))
      .sort((a, b) => a.gap - b.gap)
    return { name: ind.name, fit, gaps }
  }).sort((a, b) => b.fit - a.fit)

  return ranked.map((ind, i) => {
    const badge = ind.fit >= 90 ? '● 强烈推荐' : ind.fit >= 75 ? '● 较匹配' : '● 待发展'
    const topGap = ind.gaps.slice(0, 2)
    const gapText = topGap.map(g => `${g.label}（差 ${Math.abs(g.gap)} 分）`).join('、')
    return `**${i + 1}. ${ind.name}** ｜ 匹配度 **${ind.fit}%** ${badge}\n> 你的差距在：${gapText}`
  }).join('\n\n')
}

