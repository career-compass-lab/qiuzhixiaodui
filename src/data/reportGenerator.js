  const bar = '█'.repeat(Math.round(score / 10)) + '░'.repeat(10 - Math.round(score / 10))

  const analysis = getDimensionAnalysis(key, level, score)
  const workplace = getDimensionWorkplace(key, level)
  const tips = getDimensionTips(key, level)

